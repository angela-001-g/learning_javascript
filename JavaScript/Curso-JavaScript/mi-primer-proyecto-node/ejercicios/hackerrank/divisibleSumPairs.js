function divisibleSumPairs(n, k, ar) { 
    let count = 0;
     for (let i = 0; i < n; i++) {
         for (let j = 0; j < i; j++) {
             let res = ar[i] + ar[j]; 
             if (res % k === 0) { 
                count++;
             } 
            } 
        }
    return count
}

